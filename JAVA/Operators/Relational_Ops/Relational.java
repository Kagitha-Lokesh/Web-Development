class Relational{
	public static void main (String[] args){
		//>,<,>=,<=,==,!=
		int a=10, b=30;
		System.out.println(a +" is less than "+ b +" : "+(a<b));
		System.out.println(a +" is less than "+ b +" : "+(a>b));
		System.out.println(a +" is less than or equal to"+ b +" : "+(a<=b));
		System.out.println(a +" is less than or equal to"+ b +" : "+(a>=b));
		System.out.println(a +" is equal to"+ b +" : "+(a==b));
		System.out.println(a +" is equal to"+ b +" : "+(a!=b));
	}
}